    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if (!checkEmpty(txtID)) {
                JOptionPane.showMessageDialog(this, "Vui long nhap ma can xoa");
                return;
            } else if (!data.checkID(txtID.getText())) {
                JOptionPane.showMessageDialog(this, "Nhap ma sinh dinh dang");
                return;
            } else {
                if (message("Ban co muon xoa sinh vien nay khong") == JOptionPane.YES_OPTION) {
                    if (sql.delete(txtID.getText())) {
                        JOptionPane.showMessageDialog(this, "Xoa Thành Công");
                        showForm(0);
                    } else {
                        JOptionPane.showMessageDialog(this, "Sinh vien không tô tai");
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        sv.setID(txtID.getText());
        sv.setName(txtName.getText());
        sv.setDate(txtBirth.getText());
        sv.setPhone(txtPhone.getText());
        sv.setGender(isGender());
        sv.setAddress(txtAddress.getText());
        try {
            if (checkForm()) {
                if (sql.findID(txtID.getText()) != null) {

                    if (message("Ban co muon cap nhat sinh vien nay") == JOptionPane.YES_OPTION) {
                        if (sql.update(sv)) {
                            JOptionPane.showMessageDialog(this, "Cap nhat thanh cong");
                            showForm(0);
                        } else {
                            JOptionPane.showMessageDialog(this, "Cap Nhat that bai");
                        }
                    }
                } else {
                    if (message("Sinh vien không ton tai ban co muon them moi") == JOptionPane.YES_OPTION) {
                        if (sql.save(sv)) {
                            JOptionPane.showMessageDialog(this, "Them sinh vien thanh công");
                            showForm(0);
                        } else {
                            JOptionPane.showMessageDialog(this, "Them sinh vien that bai");
                        }
                    }
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnEditActionPerformed

