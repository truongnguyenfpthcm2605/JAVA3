    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (checkForm()) {
            try {
                if (sql.findID(txtID.getText()) != null) {
                    JOptionPane.showMessageDialog(this, "Trùng mã Sinh Viên");
                } else {
                    sv.setID(txtID.getText());
                    sv.setName(txtName.getText());
                    sv.setDate(txtBirth.getText());
                    sv.setPhone(txtPhone.getText());
                    sv.setGender(isGender());
                    sv.setAddress(txtAddress.getText());
                    if (sql.save(sv)) {
                        JOptionPane.showMessageDialog(this, "Luu sinh vien thanh cong");
                        btnNewActionPerformed(evt);
                        showForm(0);
                    } else {
                        JOptionPane.showMessageDialog(this, "Luu sinh vien that bai");
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

