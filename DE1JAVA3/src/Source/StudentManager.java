    public List<Studnet> getList() {
        try {
            List<Studnet> list = sql.getAllData();
            if (list != null) {
                return list;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }

    public void showForm(int a) {

        sv = getList().get(a);
        txtID.setText(sv.getID());
        txtName.setText(sv.getName());
        txtBirth.setText(sv.getDate());
        txtPhone.setText(sv.getPhone());
        if (sv.isGender()) {
            rdoMale.setSelected(true);
        } else {
            rdoFemale.setSelected(true);
        }
        txtAddress.setText(sv.getAddress());
    }

    public boolean checkEmpty(JTextField a) {
        if (a.getText().isEmpty()) {
            a.setBackground(Color.yellow);
            return false;
        } else {
            a.setBackground(Color.white);
        }
        return true;
    }

    public boolean checkForm() {
        if (!checkEmpty(txtID)) {
            JOptionPane.showMessageDialog(this, "Vui Long Nhap ID");
            return false;
        } else if (!data.checkID(txtID.getText())) {
            JOptionPane.showMessageDialog(this, "Nhap ma sinh vien khong dung dinh dang");
            return false;
        } else if (!checkEmpty(txtName)) {
            JOptionPane.showMessageDialog(this, "Vui long nhap ten");
            return false;
        } else if (!checkEmpty(txtBirth)) {
            JOptionPane.showMessageDialog(this, "Khong duoc bo trong ngay sinh");
            return false;
        }else if(!data.checkDate(txtBirth.getText())){
            JOptionPane.showMessageDialog(this, "Vui long nhap ngay sinh theo dinh dang (yyyy/mm/dd)");
            return false;
        } else if (!checkEmpty(txtPhone)) {
            JOptionPane.showMessageDialog(this, "Vui long nhap so dien thoai");
            return false;
        } else if (!data.checkNumberPhone(txtPhone.getText())) {
            JOptionPane.showMessageDialog(this, "Nhap so dien thoai khong dung dinh dang");
            return false;
        } else if (txtAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui long nhap dia chi");
            txtAddress.setBackground(Color.yellow);
            return false;
        } else {
            txtAddress.setBackground(Color.white);
        }
        return true;
    }

    public boolean isGender() {
        return rdoMale.isSelected();
    }

    public int message(String a) {
        return JOptionPane.showConfirmDialog(this, a, "Notifycation", JOptionPane.YES_NO_OPTION);
    }

