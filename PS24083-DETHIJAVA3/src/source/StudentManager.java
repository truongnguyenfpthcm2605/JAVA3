    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (!checkEmpty(txtMaHoaDon)) {
            JOptionPane.showMessageDialog(this, "Nhap ma hoa don muon xoa");
        } else if (!data.checkID(txtMaHoaDon.getText())) {
            JOptionPane.showMessageDialog(this, "Ma hoa don sai dinh dang");
        } else {
            try {
                if (mesage("Ban chat muon xoa hoa don nay khong") == JOptionPane.YES_OPTION) {
                    if (sql.delete(txtMaHoaDon.getText())) {
                        JOptionPane.showMessageDialog(this, "Xoa Thanh Cong");
                        fillTable();
                        btnNewActionPerformed(evt);
                    } else {
                        JOptionPane.showMessageDialog(this, "Xoa Khong Thanh Cong");
                    }
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (checkForm()) {
            try {
                Store sv = new Store();
                sv.setMaHoaDon(txtMaHoaDon.getText());
                sv.setMaHang(txtMaHang.getText());
                sv.setTenHang(txtTenHang.getText());
                sv.setNgayMua(txtDate.getText());
                sv.setSoluong(Integer.parseInt(txtSoLuong.getText()));
                sv.setDongia(Float.parseFloat(txtDonGia.getText()));
                sv.setThanhtien(Float.parseFloat(txtThanhTien.getText()));
                if (sql.findID(txtMaHoaDon.getText()) == null) {
                    if (sql.save(sv)) {
                        JOptionPane.showMessageDialog(this, "Luu Thanh Cong");
                        fillTable();
                        btnNewActionPerformed(evt);
                    } else {
                        JOptionPane.showMessageDialog(this, "That Bai");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Trung Ma Hoa don");
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

