    private void txtThanhTienFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtThanhTienFocusLost
        if (checkEmpty(txtDonGia) && checkEmpty(txtSoLuong)) {
            int soluong = Integer.parseInt(txtSoLuong.getText());
            float dongia = Float.parseFloat(txtDonGia.getText());
            double sum = soluong * dongia;
            if (sum >= 5000) {
                sum = sum * 0.9;
            }
            txtThanhTien.setText(String.valueOf(sum));

        }
    }//GEN-LAST:event_txtThanhTienFocusLost

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (!checkEmpty(txtMaHoaDon)) {
            JOptionPane.showMessageDialog(this, "Nhap ma hoa don muon cap nhat");
        } else if (!data.checkID(txtMaHoaDon.getText())) {
            JOptionPane.showMessageDialog(this, "Ma hoa don sai dinh dang");
        } else {
            Store sv = new Store();
            sv.setMaHoaDon(txtMaHoaDon.getText());
            sv.setMaHang(txtMaHang.getText());
            sv.setTenHang(txtTenHang.getText());
            sv.setNgayMua(txtDate.getText());
            sv.setSoluong(Integer.parseInt(txtSoLuong.getText()));
            sv.setDongia(Float.parseFloat(txtDonGia.getText()));
            sv.setThanhtien(Float.parseFloat(txtThanhTien.getText()));
            try {
                if (checkForm()) {
                    if (sql.findID(txtMaHoaDon.getText()) != null) {
                        if (mesage("Ban co chat muon cap nhat") == JOptionPane.YES_OPTION) {
                            if (sql.update(sv)) {
                                JOptionPane.showMessageDialog(this, "Cap nhat Thanh Cong");
                                fillTable();
                                btnNewActionPerformed(evt);
                            } else {
                                JOptionPane.showMessageDialog(this, "Cap nhat That Bai");
                            }
                        }
                    } else {
                        if (mesage("Sinh vien khong ton tai ban co muon them moi ") == JOptionPane.YES_OPTION) {
                            btnSaveActionPerformed(evt);
                        }
                    }
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

