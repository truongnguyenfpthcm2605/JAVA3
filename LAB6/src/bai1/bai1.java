    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            location = lblList.getSelectedRow();
            if (location == -1) {
                JOptionPane.showMessageDialog(this, "Please choose delete Object! ");
                return;
            } else {
                int choose = JOptionPane.showConfirmDialog(this, "Do you want to Delete?");
                if (choose == JOptionPane.YES_OPTION) {
                    Integer  id = (Integer) lblList.getValueAt(location, 0);
                    String delete = "delete from books where id =?";
                    Connection com = data.ConnectToSQl();
                   PreparedStatement pmst = com.prepareStatement(delete);
                   pmst.setInt(1,id );
                   pmst.executeUpdate();
                   JOptionPane.showMessageDialog(this, "Delete Successfull");
                   fillAllTable();    
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Delete fail");
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

