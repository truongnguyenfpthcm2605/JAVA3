    private void fillAllTable() {
        try {
            model = (DefaultTableModel) lblList.getModel();
            String sql = "select * from books where title like ?";
            Connection com = data.ConnectToSQl();
            PreparedStatement pmst = com.prepareStatement(sql);
            String title = "%";
            if (!txtTilttle.getText().isEmpty()) {
                title += txtTilttle.getText() + "%";
            }
            pmst.setString(1, title);
            ResultSet rs = pmst.executeQuery();
            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getFloat(3)});
            }
            model.fireTableDataChanged();
            rs.close();
            pmst.close();
            com.close();

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

