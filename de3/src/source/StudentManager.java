    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        if (!checkEmpty(txtID)) {
            JOptionPane.showMessageDialog(this, "Nhap ma sinh vien muon cap nhat");
        } else if (!data.checkID(txtID.getText())) {
            JOptionPane.showMessageDialog(this, "Ma sinh vien sai dinh dang");
        } else {
            Student sv = new Student();
            sv.setId(txtID.getText());
            sv.setName(txtName.getText());
            sv.setEmail(txtEmail.getText());
            try {
                if (sql.findID(txtID.getText()) != null) {
                    if (mesage("Ban co chat muon cap nhat") == JOptionPane.YES_OPTION) {
                        sql.update(sv);
                        JOptionPane.showMessageDialog(this, "Cap nhat Thanh Cong");
                        fillTable();
                    }
                } else {
                    if (mesage("Sinh vien khong ton tai ban co muon them moi ") == JOptionPane.YES_OPTION) {
                        sql.save(sv);
                        JOptionPane.showMessageDialog(this, "Them Moi Thanh Cong");
                        fillTable();
                    }
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (!checkEmpty(txtID)) {
            JOptionPane.showMessageDialog(this, "Nhap ma sinh vien muon xoa");
        } else if (!data.checkID(txtID.getText())) {
            JOptionPane.showMessageDialog(this, "Ma sinh vien sai dinh dang");
        } else {
            try {
                if (mesage("Ban chat muon xoa sinh vien nay khong") == JOptionPane.YES_OPTION) {
                    if (sql.delete(txtID.getText())) {
                        JOptionPane.showMessageDialog(this, "Xoa Thanh Cong");
                        fillTable();
                    } else {
                        JOptionPane.showMessageDialog(this, "Xoa Khong Thanh Cong");
                    }
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

