    public boolean checkEmpty(JTextField a) {
        if (a.getText().isEmpty()) {
            a.setBackground(Color.yellow);
            return false;
        } else {
            a.setBackground(Color.white);
        }
        return true;
    }

    public boolean checkForm() {
        if (!checkEmpty(txtID)) {
            JOptionPane.showMessageDialog(this, "Vui Long Nhap ID");
            return false;
        } else if (!data.checkID(txtID.getText())) {
            JOptionPane.showMessageDialog(this, "Nhap ma sinh vien khong dung dinh dang");
            return false;
        } else if (!checkEmpty(txtName)) {
            JOptionPane.showMessageDialog(this, "Vui long nhap ten");
            return false;
        } else if (!checkEmpty(txtEmail)) {
            JOptionPane.showMessageDialog(this, "Khong duoc bo trong Email");
            return false;
        } else if (!data.checkEmail(txtEmail.getText())) {
            JOptionPane.showMessageDialog(this, "Email Khong dung dinh dang");
            return false;
        } 
        return true;
    }

    public List<Student> listData() {
        try {
            List<Student> list = sql.getAllData();
            return list;
        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }

    public String isgender(boolean a) {
        if (a) {
            return "Nam";
        }
        return "Nu";
    }

    public void addColummiIndetifyTable() {
        String[] column = {"Masv", "Ten", "Email"};
        model.setColumnIdentifiers(column);
        tblList.setModel(model);

    }

    public void fillTable() {
        List<Student> list = listData();
        model.setRowCount(0);
        list.forEach((sv) -> {
            model.addRow(new Object[]{sv.getId(), sv.getName(), sv.getEmail()});
        });
        tblList.setModel(model);
    }

    public void ShowForm(int a) {
        Student sv = listData().get(a);
        txtID.setText(sv.getId());
        txtName.setText(sv.getName());
        txtEmail.setText(sv.getEmail());
    }


    public int mesage(String a) {
        return JOptionPane.showConfirmDialog(this, a, "Notifycation", JOptionPane.YES_NO_OPTION);
    }

